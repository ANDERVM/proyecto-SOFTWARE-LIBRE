    public formularioNuevaVenta() {
        
        Calendar fecha=Calendar.getInstance();
        Conexion cnx=new Conexion();
        Connection conexion;
        java.sql.Statement consultar;
        int id = 0;
        try{
            conexion=cnx.getConexion();
            consultar=conexion.createStatement();
            ResultSet resultado=consultar.executeQuery("Select * from venta");
            while(resultado.next()){
                id=resultado.getInt("id_venta");
            }
            conexion.close();
            consultar.close();
            resultado.close();
        }catch(SQLException sql){
        }
        Venta v=new Venta();
        v.setFecha(fecha);
        initComponents();
        this.cIdVenta.setText(Integer.toString(id+1));
        this.cFechaVenta.setText(v.obtenerFechaI());
    }

