    private void cIdZapatoVentaFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_cIdZapatoVentaFocusLost
        // TODO add your handling code here:
        int id_zapato;
        if(this.cIdZapatoVenta.getText().equals("")){
            JOptionPane.showMessageDialog(this,"Ingresa un ID de ZAPATO");
        }else{
            try{
                id_zapato=Integer.parseInt(this.cIdZapatoVenta.getText());
                Zapato z=new Zapato();
                z.setidZapato(id_zapato);
                Zapato zRes=new Zapato();
                zRes=new Sistema().buscarZapato(z);
                if(zRes.getidZapato()==0){
                    JOptionPane.showMessageDialog(this,"El zapato con ID: "+id_zapato+" no esta dado de alta");
                    this.cIdZapatoVenta.setText(null);
                }else{
                    this.cColorZapatoVenta.setText(zRes.getColor());
                    this.cMarcaZapatoVenta.setText(zRes.getMarca());
                    this.cTallaZapatoVenta.setText(Float.toString(zRes.getTalla()));
                    this.cPrecioZapatoVenta.setText(Float.toString(zRes.getPrecio()));
                }
            }catch(NumberFormatException nFEx){
                JOptionPane.showMessageDialog(this,"El id debe ser un valor numerico");
                this.cIdZapatoVenta.setText(null);
            }
        }   
    }//GEN-LAST:event_cIdZapatoVentaFocusLost

    private void cCantidadZapatosVentaFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_cCantidadZapatosVentaFocusLost
        // TODO add your handling code here:
        int cantidad;
        float total,precio;
        
        if(this.cCantidadZapatosVenta.getText().equals("")){
            JOptionPane.showMessageDialog(this,"Debe ingresar un numero de pares ");
        }else{
            try{
                cantidad=Integer.parseInt(this.cCantidadZapatosVenta.getText());
                if(this.cPrecioZapatoVenta.getText().equals("")){
                    JOptionPane.showMessageDialog(this,"El campo de PRECIO UNITARIO no ha sido llenado");
                }else{
                    precio=Float.parseFloat(this.cPrecioZapatoVenta.getText());
                    total=precio*cantidad;
                    this.cTotalVenta.setText(Float.toString(total));
                }
            }catch(NumberFormatException n){
                JOptionPane.showMessageDialog(this,"La cantidad debe ser un valor entero");
                this.cCantidadZapatosVenta.setText(null);
            }
        }
    }//GEN-LAST:event_cCantidadZapatosVentaFocusLost

