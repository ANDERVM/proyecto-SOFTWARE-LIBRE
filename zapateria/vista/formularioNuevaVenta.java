    private void bGuardarVentaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bGuardarVentaActionPerformed
        // TODO add your handling code here:
        int i=0,iRes;
        if(this.cCantidadZapatosVenta.getText().equals("")){
            i++;
        }else;
        if(this.cTotalVenta.getText().equals("")){
            i++;
        }else;
        if(i==0){
            Venta v=new Venta();
            v.setIdVenta(Integer.parseInt(this.cIdVenta.getText()));
            Cliente c=new Cliente();
            c.setRFC(this.cRFCVenta.getText());
            v.setCliente(c);
            Zapato z=new Zapato();
            z.setidZapato(Integer.parseInt(this.cIdZapatoVenta.getText()));
            v.setZapato(z);
            v.setCantidad(Integer.parseInt(this.cCantidadZapatosVenta.getText()));
            v.setTotalVenta(Float.parseFloat(this.cTotalVenta.getText()));
            Calendar fecha1=Calendar.getInstance();
            v.setFecha(fecha1);
            iRes=new Sistema().nuevaVenta(v);
            if(iRes==0){
                JOptionPane.showMessageDialog(this,"Los datos fueron guardados correctamente");
                this.setVisible(false);
                new menuPrincipal().setVisible(true);
            }else{
                JOptionPane.showMessageDialog(this,"Ocurrio un error mientras se guardaban los datos");
            }
        }else{
            JOptionPane.showMessageDialog(this,"Faltan campos por llenar");
    }
        
    }//GEN-LAST:event_bGuardarVentaActionPerformed

