    private void bGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bGuardarActionPerformed
        // TODO add your handling code here:
        int r,i=0;
        if(this.campoApellidos.getText().equals("")){
            i++;
        }else;
        if(this.campoMarca.getText().equals("")){
            i++;
        }else;
        if(this.campoNombre.getText().equals("")){
            i++;
        }else;
        if(this.campoTelefono.getText().equals("")){
            i++;
        }else;
        if(this.campoDireccion.getText().equals("")){
            i++;
        }else;
        if(this.campoEmail.getText().equals("")){
            i++;
        }else;
        if(this.campoIdZapato.getText().equals("")){
            i++;
            
        }else;
        if(this.campoCantidad.getText().equals("")){
            i++;
        }else;
        if(i==0){
            Proveedor p=new Proveedor();
            //p.setId(Integer.parseInt(campoId.getText()));
            p.setNombre(campoNombre.getText());
            p.setApellidos(campoApellidos.getText());
            p.setCelular(00000000);
            p.setTelefono(Integer.parseInt(campoTelefono.getText()));
            p.setDireccion(campoDireccion.getText());
            p.setEmail(campoEmail.getText());
            p.setMarca(campoMarca.getText());
            p.setCantidad(Integer.parseInt(campoCantidad.getText()));
            Zapato z=new Zapato();
            z.setidZapato(Integer.parseInt(this.campoIdZapato.getText()));
            p.setZapato(z);
            r=new Sistema().agregarProveedor(p);
            if(r==1){
                JOptionPane.showMessageDialog(this,"Error: El zapato no esta dado de alta");
            }else{
                JOptionPane.showMessageDialog(this,"Los datos se guardaron correctamente");
                this.setVisible(false);
                new menuPrincipal().setVisible(true);
            }
        }else{
            JOptionPane.showMessageDialog(this,"Faltan campos por llenar");
        }
        
    }//GEN-LAST:event_bGuardarActionPerformed

