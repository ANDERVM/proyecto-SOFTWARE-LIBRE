    public formularioNuevoProveedor() throws SQLException {
        Conexion c=new Conexion();
        Connection conexion=c.getConexion();
        java.sql.Statement declaracion;
        int id = 0;
            declaracion= conexion.createStatement();
            ResultSet resultado=declaracion.executeQuery("Select * from proveedor");
            while(resultado.next()){
                id=resultado.getInt("id_proveedor");
            }
            conexion.close();
            declaracion.close();
            resultado.close();
        initComponents();  
        campoId.setText(Integer.toString(id+1));
    }

